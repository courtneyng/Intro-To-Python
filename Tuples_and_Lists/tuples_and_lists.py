# Tuple Example
courtney = ("Courtney", "Ng", 2002, "glasses", "red", "piano")

for i in range(0,5):
    print(courtney[i])

print(courtney)


# List Example
yentruoc = ["Yentruoc", "Gn", 2002, "sessalg", "der"]

for i in range(1,5):
    print(yentruoc[i])

print(yentruoc)

yentruoc.append("onaip")

print(yentruoc)
print("Done")

